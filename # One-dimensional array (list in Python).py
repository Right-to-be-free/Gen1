# One-dimensional array (list in Python)
numbers = [1, 2, 3, 4, 5]
print(numbers[2])  # Output: 3

print(5)
print(3 + 2)

# 2D array (matrix)
matrix = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]
print(matrix[1][2])  # Output: 6
print(matrix[4 - 2][3 - 2])  # Output: 8
print(matrix[2][1])  # Output: 8

# Dynamic array (Python list)
arr = [10, 20, 30]
arr.append(40)  # Adds an element dynamically
arr.remove(20)  # Removes an element
print(arr)  # Output: [10, 30, 40]
print(arr[1])  # Output: 30
arr.append (60)
print(arr)


